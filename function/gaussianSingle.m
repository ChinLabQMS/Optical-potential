function E = gaussianSingle(Power,Waist,Wave,X,Y,Z)
% E/X/Y/Z: N-by-1 array
    c = 2.99792458*10^8;
    epsilon0 = 8.854187817*10^-12;

    E = 2*sqrt(Power/(pi*c*epsilon0*Waist(1)*Waist(end)))*10^(9/2)./ ...
        sqrt(1+(Wave*Z/(pi*Waist(1)*Waist(end))).^2).* ...
        exp(-X.^2./(Waist(1)^2+(Wave^2*Z.^2)/(pi^2*Waist(1)^2))).* ... 
        exp(-Y.^2./(Waist(end)^2+(Wave^2*Z.^2)/(pi^2*Waist(end)^2))).* ... 
        exp(-1i*((2*pi/Wave)*(Z+X.^2./(2*(Z+(pi^2*Waist(1)^4)./(Wave^2*Z)))+ ...
        Y.^2./(2*(Z+(pi^2*Waist(end)^4)./(Wave^2*Z)))) ...
        -atan((Wave*Z)/(pi*Waist(1)*Waist(end)))));
end